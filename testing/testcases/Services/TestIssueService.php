        $popService->setReturnValueAt(1, 'get_mail', $GLOBALS['MIME_EMAIL']);
        
        // $popService->setReturnValue('get_mail', $textEmail, array(1));
        // $popService->setReturnValue('get_mail', $mimeEmail, array(2));
        
        $emailService->popService = $popService;
        $emails = $emailService->readEmailFrom('mail.lateralminds.com.au', 'marcus', 'marcus', false);
        
        $params['title'] = 'Support Email Client';
        $client = $clientService->saveClient($params);
        // Create a client and a contact for that client
        $params = array('firstname'=>'Marcus', 'email'=>'nyeholt@gmail.com', 'clientid'=>$client->id);
        $contact = $clientService->saveContact($params);

        $issueService->processIncomingEmails($emails);
        
        $issueOne = $issueService->getIssues(array('issue.title=' => 'text only'));
        $this->assertEqual(1, count($issueOne));
        $issueTwo = $issueService->getIssues(array('issue.title=' => 'This is a test with attachment'));
        $this->assertEqual(1, count($issueTwo));
        
    }
    
