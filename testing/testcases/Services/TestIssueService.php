class TestIssueService extends UnitTestCase 
{
    public function testCreateIssue()
    {
        $dbService = za()->getService('DbService');
        /* @var $dbService DbService */
        $dbService->delete('issue');
        $dbService->delete('client');
        
        $clientService = za()->getService('ClientService');
        /* @var $clientService ClientService */
        $issueService = za()->getService('IssueService');
        /* @var $issueService IssueService */
        
        $params['title'] = 'Client';
        $client = $clientService->saveClient($params);
        $project = $clientService->getClientSupportProject($client);
        
        $params = array();
        $params['clientid'] = $client->id;
        $params['title'] = 'some title';
        
        // Okay, once saving the issue, it should have a projectid
        // and 
        // The following will trigger the emailing, so need to use a mock
        // object
        Mock::generate('NotificationService');
        $notificationService = new MockNotificationService();
        $issueService->notificationService = $notificationService;
        
        $issue = $issueService->saveIssue($params);
        
        $this->assertNotNull($issue->projectid);
        $this->assertEqual($issue->projectid, $project->id);
    }
    
    public function testCreateFromEmails()
    {
        $emailService = za()->getService('EmailService');
        /* @var $emailService EmailService */
        $issueService = za()->getService('IssueService');
        /* @var $issueService IssueService */
        $clientService = za()->getService('ClientService');
        /* @var $clientService ClientService */
        
        $dbService = za()->getService('DbService');
        $dbService->delete('issue');
        $dbService->delete('file');
        $dbService->delete('client');
        $dbService->delete('contact');
        $dbService->delete('project');
        
        Mock::generate('pop3_class');
        $popService = new Mockpop3_class();
        $popService->setReturnValue('connect', true);
        $popService->setReturnValue('login', true);
        $popService->setReturnValue('get_office_status', array('count_mails'=>2, 1 => array('size'=>2000), 2=> array('size'=>2000)));
